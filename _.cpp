#include <bits/stdc++.h>
using namespace std;
using ll = long long;
using vll = vector<ll>;
using vi = vector<int>;
using vvi = vector<vector<int>>;
using vvl = vector<vector<ll>>;

int main() {
	ios::sync_with_stdio(false);
	cin.tie(0);

	int t; cin>>t;
	while (t--) {
		int n;
		cin>>n;
		vll v(n);
		for (auto &it: v) cin>>it;
	}

	return 0;
}

